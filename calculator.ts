class calculator {
    plus(a: number, b: number): number {
        return a+b;
    }

    minus(a: number, b: number): number {
        return a-b;
    }
    
    multiply(a: number, b: number): number {
        return a*b;
    }

    divide(a: number, b: number): number {
        return a/b;
    }
}

export { calculator }
